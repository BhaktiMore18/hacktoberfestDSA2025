#include <iostream>
using namespace std;

int main(){
    
    //AND
    cout<<(3&5)<<endl;
    //Or
    cout<<(3|5)<<endl;
    //XOR
    cout<<(3^5)<<endl;

    //NOT operator
    cout<<(~6)<<endl;

    //Binary left shift
    cout<<(7<<2)<<endl;
    //Binary right shift
    cout<<(7>>2)<<endl;
    

    return 0;
}